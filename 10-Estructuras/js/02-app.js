// Operador de igualdad ----------------------------------> ==
if(puntaje == '1000'){
    console.log('Si es igual');
} else {    
    console.log('No es igual');
}

    if(puntaje == 1000){
        console.log('Si es igual');
    } else {
        console.log('No es igual');
    }

// Operador de igualdad estricta ----------------------------------> ===
if(puntaje === '1000'){
    console.log('Si es estrictamente igual');
} else {
    console.log('No es estrictamente igual');
}

    if(puntaje === 1000){
        console.log('Si es estrictamente igual');
    } else {
        console.log('No es estrictamente igual');
    }

// Operador de desigualdad ----------------------------------> !=
if(puntaje != '1000'){
    console.log('Si es diferente');
} else {    
    console.log('No es diferente');
}

    if(puntaje != 1000){
        console.log('Si es diferente');
    } else {
        console.log('No es diferente');
    }

// Operador de desigualdad estricta ----------------------------------> !==
if(puntaje !== '1000'){
    console.log('Si estrictamente diferente');
} else {
    console.log('No estrictamente diferente');
}

    if(puntaje !== 1000){
        console.log('Si estrictamente diferente');
    } else {
        console.log('No estrictamente diferente');
    }