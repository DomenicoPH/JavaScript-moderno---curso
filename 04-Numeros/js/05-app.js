// Incremento y Decremento

let puntaje = 5;

// incremento
console.log(puntaje)
console.log(puntaje++)
console.log(puntaje)

console.log(++puntaje)
console.log(++puntaje)
console.log(++puntaje)

console.log(puntaje += 2)
console.log(puntaje += 4)

// decremento
console.log(puntaje)
console.log(puntaje--)
console.log(puntaje)

console.log(--puntaje)
console.log(--puntaje)
console.log(--puntaje)

console.log(puntaje -= 2)
console.log(puntaje -= 4)